function saveExemplaryComplexChannelMasks(mname, masks)
    
    filePath = fullfile ...
        ( cspkResultsFolder() ...
        , sprintf ...
            ( "exemplaryComplexChannelMasks_%s_%d.mat" ...
            , mname ...
            , IkUtils.now ...
            ) ...
        );
        
    save(filePath, "masks")
    
end